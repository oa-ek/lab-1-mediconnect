using System;
using System.Collections.Generic;
using System.Linq;
using System.Threading.Tasks;
using Microsoft.AspNetCore.Mvc;
using Microsoft.AspNetCore.Mvc.Rendering;
using Microsoft.EntityFrameworkCore;
using Server.Data;
using Shared.Models;

namespace Client.Controllers
{
    public class ReviewsController : Controller
    {
        private readonly Context _context;

        public ReviewsController(Context context)
        {
            _context = context;
        }

        // GET: Reviews
        public async Task<IActionResult> Index()
        {
            var context = _context.Reviews.Include(r => r.Client).Include(r => r.Doctor);
            return View(await context.ToListAsync());
        }

        // GET: Reviews/Details/5
        public async Task<IActionResult> Details(string? description)
        {
            if (description == null)
            {
                return NotFound();
            }

            var review = await _context.Reviews
                .Include(r => r.Client)
                .Include(r => r.Doctor)
                .FirstOrDefaultAsync(m => m.Description == description);
            if (review == null)
            {
                return NotFound();
            }

            return View(review);
        }

        // GET: Reviews/Create
        public IActionResult Create()
        {
            ViewData["ClientID"] = new SelectList(_context.Users, "ID", "ID");
            ViewData["DoctorID"] = new SelectList(_context.Users, "ID", "ID");
            return View();
        }

        // POST: Reviews/Create
        // To protect from overposting attacks, enable the specific properties you want to bind to.
        // For more details, see http://go.microsoft.com/fwlink/?LinkId=317598.
        [HttpPost]
        [ValidateAntiForgeryToken]
        public async Task<IActionResult> Create([Bind("ID,ClientID,DoctorID,Rate,ReviewDate,Description")] Review review)
        {
            if (ModelState.IsValid)
            {
                _context.Add(review);
                await _context.SaveChangesAsync();
                return RedirectToAction(nameof(Index));
            }
            ViewData["ClientID"] = new SelectList(_context.Users, "ID", "ID", review.ClientID);
            ViewData["DoctorID"] = new SelectList(_context.Users, "ID", "ID", review.DoctorID);
            return View(review);
        }

        // GET: Reviews/Edit/5
        public async Task<IActionResult> Edit(int? id)
        {
            if (id == null)
            {
                return NotFound();
            }

            var review = await _context.Reviews.FindAsync(id);
            if (review == null)
            {
                return NotFound();
            }
            ViewData["ClientID"] = new SelectList(_context.Users, "ID", "ID", review.ClientID);
            ViewData["DoctorID"] = new SelectList(_context.Users, "ID", "ID", review.DoctorID);
            return View(review);
        }

        // POST: Reviews/Edit/5
        // To protect from overposting attacks, enable the specific properties you want to bind to.
        // For more details, see http://go.microsoft.com/fwlink/?LinkId=317598.
        [HttpPost]
        [ValidateAntiForgeryToken]
        public async Task<IActionResult> Edit(int id, [Bind("ID,ClientID,DoctorID,Rate,ReviewDate,Description")] Review review)
        {
            if (id != review.ID)
            {
                return NotFound();
            }

            if (ModelState.IsValid)
            {
                try
                {
                    _context.Update(review);
                    await _context.SaveChangesAsync();
                }
                catch (DbUpdateConcurrencyException)
                {
                    if (!ReviewExists(review.ID))
                    {
                        return NotFound();
                    }
                    else
                    {
                        throw;
                    }
                }
                return RedirectToAction(nameof(Index));
            }
            ViewData["ClientID"] = new SelectList(_context.Users, "ID", "ID", review.ClientID);
            ViewData["DoctorID"] = new SelectList(_context.Users, "ID", "ID", review.DoctorID);
            return View(review);
        }

        // GET: Reviews/Delete/5
        public async Task<IActionResult> Delete(string? description)
        {
            if (description == null)
            {
                return NotFound();
            }

            var review = await _context.Reviews
                .Include(r => r.Client)
                .Include(r => r.Doctor)
                .FirstOrDefaultAsync(m => m.Description == description);
            if (review == null)
            {
                return NotFound();
            }

            return View(review);
        }

        // POST: Reviews/Delete/5
        [HttpPost, ActionName("Delete")]
        [ValidateAntiForgeryToken]
        public async Task<IActionResult> DeleteConfirmed(string description)
        {
            var review = await _context.Reviews.FindAsync(description);
            _context.Reviews.Remove(review);
            await _context.SaveChangesAsync();
            return RedirectToAction(nameof(Index));
        }

        private bool ReviewExists(int id)
        {
            return _context.Reviews.Any(e => e.ID == id);
        }
    }
}
